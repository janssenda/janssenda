public class card {
    private int value;
    private String suit;
    private String rank;

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }
    

    public int getValue() {
        return value;
    }

  
    public void setValue(int value) {
        this.value = value;
    }

 
    public String getSuit() {
        return suit;
    }

  
    public void setSuit(String suit) {
        this.suit = suit;
    }
    
    
    
    
    
}
